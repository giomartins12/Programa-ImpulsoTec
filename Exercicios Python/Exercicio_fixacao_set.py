print()
print('\t ----Funcionarios que trabalham de noite----')
func_noite = set({'Alonso', 'Vargas', 'Rever'})
print('\t', func_noite)
print('\t Total: ', func_noite.__len__())
print()
print('\t ----Funcionarios que tem carro----')
func_carro = set({'Mariano', 'Rever', 'Vargas', 'Jair'})
print('\t', func_carro)
print('\t Total: ', func_carro.__len__())
print()
print('\t ----Funcionarios que trabalham a noite e tem carro----')
func_carro_noite = func_noite.intersection(func_carro)
print('\t', func_carro_noite)
print('\t Total: ', func_carro_noite.__len__())
print()
print('\t ----Funcionarios que trabalham de dia e tem carro----')
func_carro_dia = func_dia.intersection(func_carro)
print('\t', func_carro_dia)
print('\t Total: ', func_carro_dia.__len__())
